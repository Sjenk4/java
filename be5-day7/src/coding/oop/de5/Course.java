package coding.oop.de5;

public class Course {
	String nameCourse;
	String nameTeacher;
	String begin;
	String end;
	double fee;
	
	public Course(String nameCourse, String nameTeacher, String begin, String end, double fee) {
		super();
		this.nameCourse = nameCourse;
		this.nameTeacher = nameTeacher;
		this.begin = begin;
		this.end = end;
		this.fee = fee;
	
	}

}
